package com.winksys.fortress;

public class MessageHandlerException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2597738681702812150L;

	public MessageHandlerException() {
	}

	public MessageHandlerException(String arg0) {
		super(arg0);
	}

	public MessageHandlerException(Throwable arg0) {
		super(arg0);
	}

	public MessageHandlerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
